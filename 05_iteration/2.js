// while and do while loop

let i = 0;
while (i <= 10) {
  // console.log(i);
  i += 2;
}

let myArray = ["apple", "banana", "cherry"];

let arr = 0;
while (arr < myArray.length) {
  // console.log(myArray[arr]);
  arr++;
}

let score = 1;
do {
  console.log(score);
  score++;
} while (score <= 10);
